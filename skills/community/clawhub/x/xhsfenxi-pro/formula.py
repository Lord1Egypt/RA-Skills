# ── 路线决策 ───────────────────────────────────────────────────

def detect_route(archetype: Dict, temperature: Optional[str] = None) -> str:
    """
    根据 archetype + temperature 决定走哪条公式路线。
    返回: "B_HEALING" / "BA_IRONY" / "A" / "C"
    """
    typ = archetype.get("type", "B")
    primary = typ[0] if typ else "B"

    # 显式指定温度优先
    if temperature == "反讽":
        if primary in ("B", "A"):
            return "BA_IRONY"
    elif temperature == "锋利":
        return "C"
    elif temperature == "治愈":
        if primary == "B":
            return "B_HEALING"

    # 默认按 archetype
    if primary == "A":
        # A 型默认无路线区分，用 A_MODELS
        return "A"
    if primary == "C":
        return "C"
    # B 型默认治愈系，但若是 B+A 混合则建议反讽路线
    if "+A" in typ or "A+" in typ:
        # 混合型默认走反讽（更可能是B+A 反讽系），但允许 temperature 覆盖
        return "BA_IRONY"
    return "B_HEALING"


def get_models_for_route(route: str) -> List[Dict]:
    return {
        "B_HEALING": B_HEALING_MODELS,
        "BA_IRONY": BA_IRONY_MODELS,
        "A": A_MODELS,
        "C": C_MODELS,
    }.get(route, B_HEALING_MODELS)


