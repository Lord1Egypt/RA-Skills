def _build_portfolio_created_email(
    portfolio_name: str,
    method: str,
    weights: dict,
    expected_return: float,
    volatility: float,
    sharpe: float,
    risk_free_rate: float,
    investment: float,
    quantities: dict,
) -> tuple[str, str]:
    metrics = _metrics_html([
        (f"{expected_return * 100:.2f}%", "Expected Return"),
        (f"{volatility * 100:.2f}%", "Volatility"),
        (f"{sharpe:.3f}", "Sharpe Ratio"),
        (f"{risk_free_rate * 100:.2f}%", "Risk-Free Rate"),
    ])

    headers = ["Asset", "Weight", "Price", "Shares"]
    rows = []
    for ticker, data in sorted(quantities.items(), key=lambda x: x[1]["weight"], reverse=True):
        rows.append([
            ticker,
            f"{data['weight'] * 100:.2f}%",
            f"${data['price']:,.2f}",
            str(data["whole_shares"]),
        ])

    total_cost = sum(d["whole_shares"] * d["price"] for d in quantities.values())
    residual = investment - total_cost

    body = (
        f"{metrics}\n"
        f"<h3>Portfolio Weights</h3>\n"
        f"{_table_html(headers, rows)}\n"
        f'<p style="color:#4a5568;font-size:0.9em;">'
        f"Investment: ${investment:,.2f} &nbsp;|&nbsp; "
        f"Residual cash: ${residual:,.2f}</p>"
    )
    html = _wrap_email_html(portfolio_name, f"Portfolio Created &mdash; {method}", body)

    plain_lines = [
        f"Portfolio Created: {portfolio_name}",
        f"Method: {method}",
        f"Expected Return: {expected_return:.2%}  |  Volatility: {volatility:.2%}  |  Sharpe: {sharpe:.3f}",
        f"Risk-Free Rate: {risk_free_rate:.2%}",
        "",
        "Weights:",
    ]
    for ticker, data in sorted(quantities.items(), key=lambda x: x[1]["weight"], reverse=True):
        plain_lines.append(
            f"  {ticker}: {data['weight']:.2%}  ${data['price']:,.2f}  {data['whole_shares']} shares"
        )
    plain_lines.append(f"\nInvestment: ${investment:,.2f}  |  Residual cash: ${residual:,.2f}")

    return html, "\n".join(plain_lines)


def _build_backtest_complete_email(
    portfolio_name: str,
    recommended_strategy: str,
    recommendation_reason: str,
    strategies: dict,
) -> tuple[str, str]:
    callout = (
        f'<div class="callout">'
        f"<strong>Recommended: {recommended_strategy}</strong><br>"
        f"{recommendation_reason}</div>"
    )

    headers = ["Strategy", "Return", "CAGR", "Sharpe", "Max DD", "Turnover", "Costs"]
    rows = []
    for name, s in strategies.items():
        rows.append([
            f"<strong>{name}</strong>" if name == recommended_strategy else name,
            f"{s['total_return']:.2%}",
            f"{s['cagr']:.2%}",
            f"{s['sharpe']:.3f}",
            f"{s['max_drawdown']:.2%}",
            f"{s['turnover']:.2%}",
            f"${s['costs']:,.2f}",
        ])

    body = f"{callout}\n<h3>Strategy Comparison</h3>\n{_table_html(headers, rows)}"
    html = _wrap_email_html(portfolio_name, "Backtest Complete", body)

    plain_lines = [
        f"Backtest Complete: {portfolio_name}",
        f"Recommended: {recommended_strategy}",
        f"Reason: {recommendation_reason}",
        "",
        f"{'Strategy':<15} {'Return':>8} {'CAGR':>8} {'Sharpe':>8} {'Max DD':>8}",
        "-" * 55,
    ]
    for name, s in strategies.items():
        marker = " *" if name == recommended_strategy else ""
        plain_lines.append(
            f"{name:<15} {s['total_return']:>7.2%} {s['cagr']:>7.2%} "
            f"{s['sharpe']:>7.3f} {s['max_drawdown']:>7.2%}{marker}"
        )

    return html, "\n".join(plain_lines)


def _build_rebalance_reminder_email(
    portfolio_name: str,
    needs_rebalance: bool,
    reason: str,
    max_drift: float,
    max_drift_asset: str,
    orders: list,
    estimated_cost: float,
) -> tuple[str, str]:
    if needs_rebalance:
        status_html = (
            f'<div class="callout callout-warn">'
            f"<strong>Rebalance Needed</strong><br>"
            f"{reason}<br>"
            f"Max drift: {max_drift:.2%} ({max_drift_asset})</div>"
        )

        headers = ["Action", "Asset", "Shares", "Amount", "Current", "Target"]
        rows = []
        for o in orders:
            action_color = "#38a169" if o.action == "BUY" else "#e53e3e"
            rows.append([
                f'<span style="color:{action_color};font-weight:bold">{o.action}</span>',
                o.ticker,
                str(o.shares_rounded),
                f"${abs(o.dollar_amount):,.2f}",
                f"{o.current_weight:.2%}",
                f"{o.target_weight:.2%}",
            ])

        body = (
            f"{status_html}\n"
            f"<h3>Recommended Orders</h3>\n"
            f"{_table_html(headers, rows)}\n"
            f'<p style="color:#4a5568;font-size:0.9em;">'
            f"Estimated transaction cost: ${estimated_cost:,.2f}</p>"
        )
    else:
        body = (
            f'<div class="callout">'
            f'<strong style="color:#38a169;">No Rebalance Needed</strong><br>'
            f"Max drift: {max_drift:.2%} ({max_drift_asset})</div>"
        )

    html = _wrap_email_html(portfolio_name, "Rebalance Check", body)

    plain_lines = [f"Rebalance Check: {portfolio_name}"]
    if needs_rebalance:
        plain_lines += [
            f"Status: REBALANCE NEEDED",
            f"Reason: {reason}",
            f"Max drift: {max_drift:.2%} ({max_drift_asset})",
            "",
            "Recommended Orders:",
        ]
        for o in orders:
            plain_lines.append(
                f"  {o.action} {o.ticker}: {o.shares_rounded} shares (${abs(o.dollar_amount):,.2f})"
            )
        plain_lines.append(f"\nEstimated cost: ${estimated_cost:,.2f}")
    else:
        plain_lines += [
            "Status: No rebalance needed",
            f"Max drift: {max_drift:.2%} ({max_drift_asset})",
        ]

    return html, "\n".join(plain_lines)


def _build_performance_report_email(
    portfolio_name: str,
    total_value: float,
    portfolio_return: float,
    benchmark_return: float | None,
    sharpe: float,
    cagr: float,
    max_drawdown: float,
    volatility: float,
    live_days: int,
    rebalance_count: int,
) -> tuple[str, str]:
    ret_class = "positive" if portfolio_return >= 0 else "negative"
    bm_str = f"Benchmark: {benchmark_return:.2%}" if benchmark_return is not None else ""
    summary = (
        f'<div class="callout">'
        f'<strong>Total Value: ${total_value:,.2f}</strong> &nbsp;|&nbsp; '
        f'Return: <span class="{ret_class}">{portfolio_return:.2%}</span>'
    )
    if bm_str:
        summary += f" &nbsp;|&nbsp; {bm_str}"
    summary += f"<br>Live: {live_days} days &nbsp;|&nbsp; Rebalances: {rebalance_count}</div>"

    metrics = _metrics_html([
        (f"{cagr * 100:.2f}%", "CAGR"),
        (f"{sharpe:.3f}", "Sharpe"),
        (f"{volatility * 100:.2f}%", "Volatility"),
        (f"{max_drawdown * 100:.2f}%", "Max Drawdown"),
    ])

    body = f"{summary}\n{metrics}"
    html = _wrap_email_html(portfolio_name, "Performance Report", body)

    plain_lines = [
        f"Performance Report: {portfolio_name}",
        f"Total Value: ${total_value:,.2f}",
        f"Return: {portfolio_return:.2%}",
    ]
    if benchmark_return is not None:
        plain_lines.append(f"Benchmark: {benchmark_return:.2%}")
    plain_lines += [
        f"CAGR: {cagr:.2%}  |  Sharpe: {sharpe:.3f}  |  Vol: {volatility:.2%}  |  Max DD: {max_drawdown:.2%}",
        f"Live: {live_days} days  |  Rebalances: {rebalance_count}",
    ]

    return html, "\n".join(plain_lines)


