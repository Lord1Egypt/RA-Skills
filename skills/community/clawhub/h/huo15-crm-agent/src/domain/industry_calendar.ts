export function thisMonth(date = new Date()): MonthEntry {
  const m = date.getMonth() + 1;
  return CALENDAR[m - 1];
}

export function nextMonths(n: number, date = new Date()): MonthEntry[] {
  const m = date.getMonth();
  return Array.from({ length: n }, (_, i) => CALENDAR[(m + i) % 12]);
}
