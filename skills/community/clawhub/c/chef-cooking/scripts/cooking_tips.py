def search_tips(query: str = None, category: str = None) -> dict:
    """搜索烹饪技巧"""
    results = []

    if category:
        tips = TIPS_DB.get(category, [])
        if query:
            tips = [t for t in tips if query.lower() in t["q"].lower() or query.lower() in t["a"].lower()]
        results = tips
    elif query:
        for cat, tips in TIPS_DB.items():
            for t in tips:
                if query.lower() in t["q"].lower() or query.lower() in t["a"].lower():
                    results.append({**t, "category": cat})
    else:
        # 列出所有分类
        return {"categories": {cat: len(tips) for cat, tips in TIPS_DB.items()}}

    return {"query": query, "category": category, "results": results, "count": len(results)}


def main():
    parser = argparse.ArgumentParser(description="烹饪技巧知识库")
    parser.add_argument("--query", type=str, help="搜索关键词")
    parser.add_argument("--category", type=str, help="技巧分类（刀工/火候/调味/食材处理/器具使用/常见失误/技法进阶/厨房效率）")
    parser.add_argument("--json", action="store_true", help="JSON 格式输出")
    args = parser.parse_args()

    result = search_tips(args.query, args.category)

    if args.json:
        print(json.dumps(result, ensure_ascii=False, indent=2))
    else:
        if "categories" in result:
            print("\n📚 烹饪技巧知识库分类：\n")
            for cat, count in result["categories"].items():
                print(f"  • {cat}: {count}条技巧")
            print(f"\n  总计: {sum(result['categories'].values())} 条技巧\n")
            print("💡 使用 --query '关键词' 搜索，--category '分类' 筛选")
            return

        print(f"\n🔍 搜索: {args.query or '全部'} | 分类: {args.category or '全部'}")
        print(f"📊 找到 {result['count']} 条:\n")

        for i, t in enumerate(result["results"], 1):
            cat_tag = f" [{t.get('category', '')}]" if t.get('category') else ""
            print(f"{i}. ❓ {t['q']}{cat_tag}")
            print(f"   💡 {t['a']}")
            print()


if __name__ == "__main__":
    main()
