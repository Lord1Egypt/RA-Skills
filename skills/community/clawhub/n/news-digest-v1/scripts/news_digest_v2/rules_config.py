def clean_summary_text(summary, max_length=300):
    """
    清理摘要（使用预编译的正则）
    
    Args:
        summary: 原始摘要
        max_length: 最大长度
    
    Returns:
        清理后的摘要
    """
    if not summary:
        return ''
    
    # 应用所有电头过滤规则（预编译正则已包含 MULTILINE 标志）
    for regex in DATATYPE_REGEX:
        summary = regex.sub('', summary)
    
    # 清理多余空行和空格
    summary = re.sub(r'\n\s*\n', '\n', summary)
    summary = re.sub(r'  +', ' ', summary)
    
    # 智能截断（在完整句子结尾处截断，避免半截句）
    if len(summary) > max_length:
        truncated = summary[:max_length]
        # 从后往前找最近一个句号
        cut_pos = -1
        for sep in ['。', '！', '？']:
            pos = truncated.rfind(sep)
            if pos > len(truncated) * 0.4:  # 至少保留 40% 内容
                cut_pos = pos + 1
                break
        
        if cut_pos > 0:
            summary = truncated[:cut_pos]  # 完整句子结尾，不加 ...
        else:
            # 没有句号才加 ...
            summary = summary[:max_length] + '...'
    
    return summary.strip()


def is_excluded_by_topic(text):
    """
    检查是否为排除主题
    
    Args:
        text: 标题 + 内容
    
    Returns:
        bool: 是否排除
    """
    text_lower = text.lower()
    
    for category, keywords in EXCLUDED_TOPICS.items():
        if any(kw in text_lower for kw in keywords):
            # 投诉类如果有产业背景则保留
            if category == 'complaint':
                if any(kw in text_lower for kw in ['产业', '行业', '市场', '政策', '监管', '企业']):
                    continue
            return True
    
    return False


