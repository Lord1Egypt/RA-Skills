def get_pool(core_only: bool = True) -> list:
    """
    获取股票池

    Args:
        core_only: True 返回~150只核心股，False 返回全量

    Returns:
        [code, ...] 股票代码列表
    """
    if core_only:
        return [s[0] for s in FALLBACK_POOL]

    # 尝试动态获取
    dynamic = get_a_share_list()
    if dynamic:
        return [s[0] for s in dynamic]
    return [s[0] for s in FALLBACK_POOL]


def get_name_map() -> dict:
    """返回 {code: name} 字典"""
    name_map = {s[0]: s[1] for s in FALLBACK_POOL}
    # 尝试补充动态数据
    dynamic = get_a_share_list()
    if dynamic:
        for c, n in dynamic:
            if c not in name_map:
                name_map[c] = n
    return name_map
