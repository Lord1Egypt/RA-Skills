## 渐进式读取

| Level | 读什么 | 什么时候 |
|-------|--------|----------|
| L0 | 本文件（MEMORY.md） | **每次启动必读** |
| L1 | 对应卦位目录（1-2 个） | 根据对话话题按需读取 |
| L2 | `memory/bagua/README.md` | 需要深入理解分类、卦变关联时才读 |

**示例**：用户问"昨天我们讨论了什么决策？" → L0 定位到坎卦 → L1 读 `kan/` → 结束，不需要 L2。

