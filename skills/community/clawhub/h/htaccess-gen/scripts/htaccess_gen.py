#!/usr/bin/env python3
"""Generate production-ready .htaccess files for Apache web servers."""

import argparse
import sys
from pathlib import Path
from datetime import datetime


def section(title):
    return f"\n# ── {title} {'─' * (60 - len(title))}\n"


def generate(args):
    parts = []
    parts.append(f"# .htaccess generated by htaccess-gen — {datetime.now().strftime('%Y-%m-%d %H:%M')}")

    # HTTPS redirect
    if args.https:
        parts.append(section("Force HTTPS"))
        parts.append("RewriteEngine On")
        parts.append("RewriteCond %{HTTPS} off")
        parts.append("RewriteRule ^ https://%{HTTP_HOST}%{REQUEST_URI} [L,R=301]")

    # WWW / no-www
    if args.www:
        domain = args.domain or "example.com"
        parts.append(section("Force www"))
        parts.append("RewriteEngine On")
        parts.append(f"RewriteCond %{{HTTP_HOST}} !^www\\. [NC]")
        parts.append(f"RewriteRule ^ https://www.%{{HTTP_HOST}}%{{REQUEST_URI}} [L,R=301]")
    elif args.no_www:
        parts.append(section("Remove www"))
        parts.append("RewriteEngine On")
        parts.append("RewriteCond %{HTTP_HOST} ^www\\.(.*) [NC]")
        parts.append("RewriteRule ^ https://%1%{REQUEST_URI} [L,R=301]")

    # Security headers
    if args.security_headers:
        parts.append(section("Security Headers"))
        parts.append("<IfModule mod_headers.c>")
        parts.append('    Header set X-Frame-Options "SAMEORIGIN"')
        parts.append('    Header set X-Content-Type-Options "nosniff"')
        parts.append('    Header set X-XSS-Protection "1; mode=block"')
        parts.append('    Header set Referrer-Policy "strict-origin-when-cross-origin"')
        parts.append('    Header set Permissions-Policy "camera=(), microphone=(), geolocation=()"')
        if args.https:
            parts.append('    Header set Strict-Transport-Security "max-age=31536000; includeSubDomains"')
        parts.append("</IfModule>")

    # CORS
    if args.cors:
        origin = args.cors_origin or "*"
        parts.append(section("CORS Headers"))
        parts.append("<IfModule mod_headers.c>")
        parts.append(f'    Header set Access-Control-Allow-Origin "{origin}"')
        parts.append('    Header set Access-Control-Allow-Methods "GET, POST, OPTIONS, PUT, DELETE"')
        parts.append('    Header set Access-Control-Allow-Headers "Content-Type, Authorization, X-Requested-With"')
        parts.append("</IfModule>")

    # Gzip compression
    if args.gzip:
        parts.append(section("Gzip Compression"))
        parts.append("<IfModule mod_deflate.c>")
        parts.append("    AddOutputFilterByType DEFLATE text/html text/plain text/css")
        parts.append("    AddOutputFilterByType DEFLATE text/javascript application/javascript application/json")
        parts.append("    AddOutputFilterByType DEFLATE text/xml application/xml application/xhtml+xml")
        parts.append("    AddOutputFilterByType DEFLATE image/svg+xml application/font-woff application/font-woff2")
        parts.append("</IfModule>")

    # Browser caching
    if args.caching:
        parts.append(section("Browser Caching"))
        parts.append("<IfModule mod_expires.c>")
        parts.append("    ExpiresActive On")
        parts.append('    ExpiresByType text/html "access plus 1 hour"')
        parts.append('    ExpiresByType text/css "access plus 1 week"')
        parts.append('    ExpiresByType application/javascript "access plus 1 week"')
        parts.append('    ExpiresByType image/jpeg "access plus 1 month"')
        parts.append('    ExpiresByType image/png "access plus 1 month"')
        parts.append('    ExpiresByType image/gif "access plus 1 month"')
        parts.append('    ExpiresByType image/webp "access plus 1 month"')
        parts.append('    ExpiresByType image/svg+xml "access plus 1 month"')
        parts.append('    ExpiresByType image/x-icon "access plus 1 year"')
        parts.append('    ExpiresByType font/woff2 "access plus 1 year"')
        parts.append('    ExpiresByType font/woff "access plus 1 year"')
        parts.append('    ExpiresByType application/font-woff "access plus 1 year"')
        parts.append("</IfModule>")

    # Custom error pages
    if args.error_pages:
        parts.append(section("Custom Error Pages"))
        parts.append("ErrorDocument 400 /errors/400.html")
        parts.append("ErrorDocument 403 /errors/403.html")
        parts.append("ErrorDocument 404 /errors/404.html")
        parts.append("ErrorDocument 500 /errors/500.html")
        parts.append("ErrorDocument 503 /errors/503.html")

    # Hotlink protection
    if args.hotlink_protection:
        domain = args.domain or "example.com"
        parts.append(section("Hotlink Protection"))
        parts.append("RewriteEngine On")
        parts.append("RewriteCond %{HTTP_REFERER} !^$")
        parts.append(f"RewriteCond %{{HTTP_REFERER}} !^https?://(www\\.)?{domain.replace('.', '\\.')} [NC]")
        parts.append("RewriteRule \\.(jpg|jpeg|png|gif|webp|svg)$ - [F,NC]")

    # IP blocking
    if args.block_ip:
        parts.append(section("IP Blocking"))
        parts.append("<RequireAll>")
        parts.append("    Require all granted")
        for ip in args.block_ip:
            parts.append(f"    Require not ip {ip}")
        parts.append("</RequireAll>")

    # Directory index
    if args.index:
        parts.append(section("Directory Index"))
        parts.append(f"DirectoryIndex {args.index}")

    output = "\n".join(parts).strip() + "\n"

    if args.output:
        Path(args.output).write_text(output)
        print(f"Written to {args.output}")
    else:
        print(output)


def redirect(args):
    code = args.type or 301
    parts = []
    parts.append("RewriteEngine On")
    parts.append(f"RewriteRule ^{args.from_path.lstrip('/')}$ {args.to} [L,R={code}]")
    print("\n".join(parts))


def rewrite(args):
    parts = []
    parts.append("RewriteEngine On")
    if args.condition:
        for cond in args.condition:
            parts.append(f"RewriteCond {cond}")
    flags = args.flags or "[L,QSA]"
    parts.append(f"RewriteRule {args.pattern} {args.target} {flags}")
    print("\n".join(parts))


def main():
    parser = argparse.ArgumentParser(description="Generate .htaccess files for Apache web servers.")
    sub = parser.add_subparsers(dest="command", help="Command")

    # generate
    gen = sub.add_parser("generate", help="Generate a complete .htaccess file")
    gen.add_argument("--https", action="store_true", help="Force HTTPS redirect")
    gen.add_argument("--www", action="store_true", help="Force www prefix")
    gen.add_argument("--no-www", action="store_true", help="Remove www prefix")
    gen.add_argument("--security-headers", action="store_true", help="Add security headers")
    gen.add_argument("--caching", action="store_true", help="Add browser caching rules")
    gen.add_argument("--gzip", action="store_true", help="Enable gzip compression")
    gen.add_argument("--error-pages", action="store_true", help="Add custom error page directives")
    gen.add_argument("--hotlink-protection", action="store_true", help="Prevent image hotlinking")
    gen.add_argument("--domain", help="Your domain name")
    gen.add_argument("--block-ip", action="append", help="Block IP/CIDR (repeatable)")
    gen.add_argument("--cors", action="store_true", help="Enable CORS headers")
    gen.add_argument("--cors-origin", help="CORS allowed origin (default: *)")
    gen.add_argument("--index", help="Directory index file")
    gen.add_argument("--output", "-o", help="Output file path")

    # redirect
    rdr = sub.add_parser("redirect", help="Generate redirect rules")
    rdr.add_argument("--from", dest="from_path", required=True, help="Source path")
    rdr.add_argument("--to", required=True, help="Destination path or URL")
    rdr.add_argument("--type", type=int, default=301, help="HTTP status: 301 or 302")

    # rewrite
    rw = sub.add_parser("rewrite", help="Generate URL rewrite rules")
    rw.add_argument("--pattern", required=True, help="Regex pattern")
    rw.add_argument("--target", required=True, help="Rewrite target")
    rw.add_argument("--flags", default="[L,QSA]", help="RewriteRule flags")
    rw.add_argument("--condition", action="append", help="RewriteCond (repeatable)")

    args = parser.parse_args()

    if not args.command:
        parser.print_help()
        sys.exit(1)

    if args.command == "generate":
        generate(args)
    elif args.command == "redirect":
        redirect(args)
    elif args.command == "rewrite":
        rewrite(args)


if __name__ == "__main__":
    main()
