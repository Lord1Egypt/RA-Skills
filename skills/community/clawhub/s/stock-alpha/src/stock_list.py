from datetime import date, timedelta

def get_a_share_list() -> list:
    """
    使用 baostock 获取 A 股全列表

    Returns:
        [(code, name), ...] 如 [("600036", "招商银行"), ...]
    """
    try:
        import baostock as bs
        from datetime import date, timedelta

        # 用最近一个交易日查询
        import pandas as pd
        # 取最近一个交易日（如果不是交易日，baostock会自动找最近的交易日）
        ref_date = (date.today() - timedelta(days=3)).strftime("%Y-%m-%d")

        bs.login()
        rs = bs.query_all_stock(ref_date)
        stocks = []
        while rs.next():
            row = rs.get_row_data()
            stocks.append(row)
        bs.logout()

        # 只取 A 股（排除指数、债券、ETF）
        a_shares = []
        for s in stocks:
            code = s[0]  # "sh.600036" or "sz.000001"
            market_code = code.lower()
            if not (market_code.startswith("sh.6") or market_code.startswith("sz.0") or market_code.startswith("sz.3")):
                continue
            if s[1] != "1":  # status code: 1 = listed
                continue
            pure_code = code.replace("sh.", "").replace("sz.", "")
            a_shares.append((pure_code, s[2]))

        return a_shares

    except Exception as e:
        print(f"[WARN] baostock stock list failed: {e}")
        return []


