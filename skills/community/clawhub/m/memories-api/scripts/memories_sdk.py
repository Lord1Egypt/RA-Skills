class WebhookCallbackHandler:
    """
    Utility for handling async webhook callbacks.
    Designed to be used within web frameworks like FastAPI or Flask.
    """
    @staticmethod
    def parse_callback(request_body: Union[str, bytes, Dict]) -> Dict[str, Any]:
        if isinstance(request_body, (str, bytes)):
            return json.loads(request_body)
        return request_body

    @staticmethod
    def handle_status(data: Dict[str, Any]):
        """Example handler logic for a webhook payload."""
        task_id = data.get("task_id")
        status = data.get("status")
        payload = data.get("data")
        
        if status == "completed":
            logger.info(f"Webhook: Task {task_id} completed successfully.")
            return payload
        elif status == "failed":
            error = data.get("error", "Unknown error")
            logger.error(f"Webhook: Task {task_id} failed: {error}")
            return None
        return None
