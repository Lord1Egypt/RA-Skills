function section(title, items) {
  if (!items || items.length === 0) {
    return `${title}\n- None\n`;
  }

  return `${title}\n${items.map((item) => `- ${item}`).join("\n")}\n`;
}

export function renderTextBrief(brief, { briefMode = "hybrid" } = {}) {
  const provenanceLine = briefMode === "lossless-only"
    ? "Generated by Mindkeeper from lossless recall context only."
    : "Generated by Mindkeeper from daily memory and lossless recall context.";

  return [
    `${brief.title} — ${brief.humanDate}`,
    provenanceLine,
    "",
    section("Recommendations", brief.summary.recommendations),
    section("What mattered", brief.summary.whatMattered),
    section("Decisions", brief.summary.decisions),
    section("Open loops", brief.summary.openLoops),
  ].join("\n");
}
