def _wrap_email_html(title: str, subtitle: str, body_content: str) -> str:
    gen_date = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC")
    return f"""\
<!DOCTYPE html>
<html lang="en">
<head><meta charset="UTF-8"><style>{_EMAIL_CSS}</style></head>
<body>
<div class="header">
  <h1>{title}</h1>
  <div class="meta">{subtitle} &mdash; {gen_date}</div>
</div>
<div class="container">
{body_content}
</div>
<div class="footer">
  Generated by MPT Portfolio Optimizer.<br>
  This is not financial advice. Past performance does not guarantee future results.
</div>
</body>
</html>"""


def _metrics_html(metrics: list[tuple[str, str]]) -> str:
    boxes = "".join(
        f'<div class="metric-box">'
        f'<div class="metric-value">{val}</div>'
        f'<div class="metric-label">{label}</div></div>'
        for val, label in metrics
    )
    return f'<div class="metric-grid">{boxes}</div>'


def _table_html(headers: list[str], rows: list[list[str]]) -> str:
    hdr = "".join(f"<th>{h}</th>" for h in headers)
    body = ""
    for row in rows:
        cells = "".join(f"<td>{c}</td>" for c in row)
        body += f"<tr>{cells}</tr>\n"
    return (
        f'<div class="card"><table>\n'
        f"<thead><tr>{hdr}</tr></thead>\n"
        f"<tbody>{body}</tbody>\n"
        f"</table></div>"
    )


