# 合并重复 key（不同位置的同一个字可能映射不同）

DEDUPED = {}

for k, v in MARS_MAP:

    if k not in DEDUPED:

        DEDUPED[k] = []

    if isinstance(v, str) and v not in DEDUPED[k]:

        DEDUPED[k].append(v)

    elif isinstance(v, (list, tuple, set)):

        for item in v:

            if item not in DEDUPED[k]:

                DEDUPED[k].append(item)

    # 过滤空列表

    if not DEDUPED[k]:

        DEDUPED.pop(k)





def convert(text: str, probability: float = 1.0) -> str:

    result = []

    for char in text:

        if char in DEDUPED and random.random() < probability:

            result.append(random.choice(DEDUPED[char]))

        else:

            result.append(char)

    return "".join(result)





def convert_all(text: str) -> str:

    result = []

    for char in text:

        if char in DEDUPED:

            result.append(random.choice(DEDUPED[char]))

        else:

            result.append(char)

    return "".join(result)





def show_mapping(char: str) -> list:

    return DEDUPED.get(char, [char])





def main():

    args = sys.argv[1:]

    if not args:

        print("用法: mars_text.py <文本> [--all] [--prob 0.7] [--seed N] [--map 字] [--json]")

        sys.exit(1)



    prob = 1.0

    seed = None

    mode = "prob"

    query_char = None

    json_output = False

    text_parts = []



    i = 0

    while i < len(args):

        if args[i] == "--all":

            mode = "all"

        elif args[i] == "--prob" and i + 1 < len(args):

            prob = float(args[i + 1])

            i += 1

        elif args[i] == "--seed" and i + 1 < len(args):

            seed = int(args[i + 1])

            i += 1

        elif args[i] == "--map" and i + 1 < len(args):

            query_char = args[i + 1]

            i += 1

        elif args[i] == "--json":

            json_output = True

        else:

            text_parts.append(args[i])

        i += 1



    text = "".join(text_parts)



    if query_char:

        mapping = show_mapping(query_char)

        if len(mapping) == 1 and mapping[0] == query_char:

            print(f"「{query_char}」暂无替代写法")

        else:

            print(f"「{query_char}」→ {' / '.join(mapping)}")

        return



    if seed is not None:

        random.seed(seed)



    if mode == "all":

        result = convert_all(text)

    else:

        result = convert(text, probability=prob)



    if json_output:

        print(json.dumps({"original": text, "mars_text": result, "mode": mode, "probability": prob}, ensure_ascii=False))

    else:

        print(result)





if __name__ == "__main__":

    main()
