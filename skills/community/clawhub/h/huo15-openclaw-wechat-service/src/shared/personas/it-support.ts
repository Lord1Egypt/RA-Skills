/**
 * 内置 preset 注册表。
 *  - it-support：通用 IT 学习客服（开源 / 教学场景）
 *  - huo15-customer：火一五·逸寻智库公众号客服（含 6 产品 / 4 服务 / 课程库 / 转化路径）
 */
export const BUILT_IN_PERSONAS: Record<string, string> = {
  "it-support": IT_SUPPORT_PERSONA,
  "huo15-customer": HUO15_CUSTOMER_PERSONA,
};

/**
 * 解析 preset 名 → instructions 文本。
 * 未知 preset 名 / "none" / "" → undefined（不注入）。
 */
export function resolvePersonaInstructions(preset?: string): string | undefined {
  if (!preset) return IT_SUPPORT_PERSONA; // 默认 it-support
  const key = preset.trim().toLowerCase();
  if (!key || key === "none" || key === "off") return undefined;
  return BUILT_IN_PERSONAS[key];
}
