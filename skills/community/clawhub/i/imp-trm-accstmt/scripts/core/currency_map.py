def get_currency_name(code: str) -> str:
    """
    根据币种编码获取中文名称

    Args:
        code: 币种编码，如 'CNY', 'USD', 'EUR' 等

    Returns:
        币种中文名称，如果未找到则返回原始编码
    """
    if not code:
        return ''
    return CURRENCY_MAP.get(code.upper(), code)


def get_currency_code(name: str) -> str:
    """
    根据币种中文名称获取编码（反向映射）

    Args:
        name: 币种中文名称

    Returns:
        币种编码，如果未找到则返回原始名称
    """
    if not name:
        return ''
    for code, cn_name in CURRENCY_MAP.items():
        if cn_name == name:
            return code
    return name
