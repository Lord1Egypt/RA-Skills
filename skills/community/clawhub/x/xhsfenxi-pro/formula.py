def _parse_likes(v) -> int:
    if not v:
        return 0
    s = str(v).replace(",", "").strip()
    if "万" in s:
        return int(float(s.replace("万", "").replace("+", "")) * 10000)
    try:
        return int(s)
    except ValueError:
        return 0


def _get_top_notes(notes: List[Any], n: int = 5):
    def get_likes(x):
        return _parse_likes(x.likes if hasattr(x, "likes") else x.get("likes"))
    return sorted(notes, key=lambda x: -get_likes(x))[:n]


def _get_title(n):
    return n.title if hasattr(n, "title") else n.get("title", "")


def _get_likes(n):
    return _parse_likes(n.likes if hasattr(n, "likes") else n.get("likes"))


