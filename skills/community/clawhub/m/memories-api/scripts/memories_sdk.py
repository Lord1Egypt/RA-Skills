    def wait_for_task(
        self, 
        task_id: str, 
        poll_interval: int = 5, 
        timeout: int = 600
    ) -> TaskStatus:
        """Poll task status until completion or timeout."""
        start_time = time.time()
        while (time.time() - start_time) < timeout:
            status = self.get_task_status(task_id)
