#!/usr/bin/env python3
"""公告正文清洗模块 - 移除重复性、程序化的模板文字

常见模板文字包括:
  1. 股票代码表头: 证券代码:xxx 证券简称:xxx 公告编号:xxx
  2. 董事会/监事会声明: "本公司及董事会全体成员保证..."
  3. 标准结语: "特此公告" + 公司名 + 日期
  4. 股票信息行: "股票简称:xxx 股票代码:xxx"
  5. PDF 页眉页脚噪声
  6. 其他监管要求的固定模板
"""

import logging
import re

logger = logging.getLogger(__name__)

# ── 模板规则列表 ──
# 每个规则是一个 (pattern, replacement) 元组
# pattern: 正则表达式（支持多行匹配）
# replacement: 替换文本（通常为空字符串）

# 日期正则：匹配 "2026年5月29日" 或 "二〇二六年五月二十九日"
_DATE_PATTERN = r"(?:\d{4}|[一二三四五六七八九十〇零百千]+)\s*年\s*(?:\d{1,2}|[一二三四五六七八九十廿卅]+)\s*月\s*(?:\d{1,2}|[一二三四五六七八九十廿卅百千]+)\s*日"

RULES = [
    # 1. 股票代码表头行
    # 例: "证券代码:600961 证券简称:株冶集团 公告编号:2026-021"
    # 或: "证券代码:601669 股票简称:中国电建 公告编号:临2026-028"
    (r"证券代码[：:]\s*\S+\s+(?:证券|股票)简称[：:]\s*\S+\s+(公告编号[：:]\s*\S+)?", ""),

    # 2. 带股票简称的标题行（如 "株洲冶炼集团股份有限公司" 这种纯公司名行）
    # 匹配独立的公司全称行（非段落开头）
    (r"^\s*\S{4,20}(股份有限)?公司\s*$", ""),

    # 3. 董事会/监事会免责声明（多个变体）
    (r"本公司及董事会全体成员保证[^。]*?(?:虚假记载|误导性陈述|重大遗漏)[^。]*?(?:法律)?责任[。，]", ""),
    (r"本公司董事会及全体董事保证[^。]*?(?:虚假记载|误导性陈述|重大遗漏)[^。]*?(?:法律)?责任[。，]", ""),
    (r"本公司及全体董事[^。]*?(?:虚假记载|误导性陈述|重大遗漏)[^。]*?(?:法律)?责任[。，]", ""),
    (r"本公司及[^。]*?监事[^。]*?(?:保证|确保)[^。]*?(?:虚假记载|误导性陈述|重大遗漏)[^。]*?(?:法律)?责任[。，]", ""),
    (r"本公司及[^。]*?高级管理人员[^。]*?(?:保证|确保)[^。]*?(?:虚假记载|误导性陈述|重大遗漏)[^。]*?(?:法律)?责任[。，]", ""),
    (r"本公司[^。]*?董事[^。]*?高级管理人员[^。]*?(?:保证|确保)[^。]*?(?:虚假记载|误导性陈述|重大遗漏)[^。]*?(?:法律)?责任[。，]", ""),
    (r"不存在[^。]*?虚假[^。]*?误导[^。]*?重大遗漏[^。]*?承担[^。]*?责任[。，]", ""),
    (r"对其内容的真实性、准确性和完整性承担法律责任[。，]", ""),
    (r"全体董事[^。]*?保证[^。]*?内容[^。]*?真实[^。]*?准确[^。]*?完整[^。]*?责任[。，]", ""),
    (r"(?:保证|确保)[^。]*?不存在[^。]*?(?:虚假|误导|遗漏)[^。]*?法律[。，]", ""),

    # 4. "特此公告" 及其后的固定结语
    # 保留"特此公告"前的实质性内容，只移除模板化的闭场
    (r"\n\s*特此公告[。\s]*\n?(?:\s*\S*?公司\s*(?:董事会|监事会)?\s*\n?)?(?:\s*" + _DATE_PATTERN + r"\s*)?", "\n"),

    # 4b. 独立日期落款行（修复：中文数字日期如 二〇二六年五月二十九日）
    (r"\n\s*" + _DATE_PATTERN + r"\s*$", ""),

    # 5. 股票信息行（出现在募集说明书等文档开头）
    # 例: "股票简称:申万宏源 股票代码:000166"
    (r"股票简称[：:]\s*\S+\s+股票代码[：:]\s*\S+", ""),

    # 6. "重要内容提示" 板块标记（保留下方内容，只移除标题）
    (r"重要内容提示[：:]\s*\n?", ""),

    # 7. PDF 页脚/分页噪声
    # "第1页 共3页" 类
    (r"\n\s*第\s*\d+\s*页\s*共\s*\d+\s*页\s*\n?", "\n"),
    # 纯数字页码行
    (r"^\s*\d+\s*$", ""),
    # PDF 水印文字
    (r"[\w\-]+\.pdf\s*\d{4}-\d{2}-\d{2}\s*\d{2}:\d{2}:\d{2}", ""),

    # 8. 年报/公告PDF中固定的"声明"板块
    (r"声\s*明\s*\n[^。]*?中国证监会[^。]*?证券交易所[^。]*?责任[。，]", ""),
    (r"声\s*明\s*\n[^。]*?本募集说明书[^。]*?真实[^。]*?准确[^。]*?完整[^。]*?责任[。，]", ""),

    # 9. 投资者关系活动记录表头
    (r"证券代码[：:]\s*\S+\s*证券简称[：:]\s*\S+\s*\n", ""),
    (r"\S+?股份有限公司投资者关系活动记录表", ""),
    (r"投资者关系活动记录表", ""),

    # 10. 地址/邮编等联系信息模板
    (r"(?:住所|地址)[：:].*?(?:\d{3,6}室|号|楼|大厦|路\d+号)", ""),
    (r"邮政编码[：:]\s*\d{6}", ""),
    (r"联系电话[：:]\s*\S+", ""),
    (r"传真[：:]\s*\S+", ""),

    # 11. 募集说明书中的固定评级/担保信息
    (r"信用评级结果[：:][^。]*?[。，]", ""),
    (r"信用评级机构[：:][^。]*?[。，]", ""),
    (r"增信措施情况[：:][^。]*?[。，]", ""),
    (r"担保或其他增信情况[：:][^。]*?[。，]", ""),
    (r"发行金额[：:][^。]*?[。，]", ""),
    (r"牵头主承销商[：:].*", ""),
    (r"联席主承销商[：:].*", ""),
    (r"簿记管理人[：:].*", ""),
    (r"受托管理人[：:].*", ""),

    # 12. 年报/股东会资料中的模板目录
    (r"目\s*录\s*\n[\s\S]*?(?=议案|一[、）]|\d[、）])", ""),

    # 13. H股表格模板文字
    (r"翌日披露報表\s*\([^)]*\)\s*", ""),
    (r"表格類別[：:]\s*\S+\s+狀態[：:]\s*\S+\s*", ""),
    (r"如上市發行人的已發行股份[^。]*?作出披露，必須填妥第一章節。", ""),

    # 14. 多余的空白行（保留最多一个连续空行）
    (r"\n{3,}", "\n\n"),
    (r"^\s*\n", ""),
]

# 编译所有规则
COMPILED_RULES = [(re.compile(p, re.MULTILINE), r) for p, r in RULES]


def clean_announcement_text(text: str) -> str:
    """清洗公告正文，移除重复性模板文字

    Args:
        text: 原始公告正文

    Returns:
        清洗后的文本
    """
    if not text:
        return ""

    cleaned = text
    for pattern, replacement in COMPILED_RULES:
        cleaned = pattern.sub(replacement, cleaned)

    cleaned = cleaned.strip()

    return cleaned


def get_stats(original: str, cleaned: str) -> dict:
    """返回清洗前后的字数对比"""
    orig_len = len(original)
    clean_len = len(cleaned)
    saved = orig_len - clean_len
    pct = (saved / orig_len * 100) if orig_len > 0 else 0
    return {
        "original_chars": orig_len,
        "cleaned_chars": clean_len,
        "saved_chars": saved,
        "saved_pct": round(pct, 1),
    }
