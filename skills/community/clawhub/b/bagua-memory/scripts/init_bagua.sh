## 归类指南

遇到新信息时，按以下规则判断写入哪个卦位：

1. **系统行为/规则/约束** → ☰ 乾（qian/）
2. **事实/知识/人名/项目信息** → ☷ 坤（kun/）
3. **近期重要事件/新发现** → ☲ 离（li/），同时记录到 ☳ 震
4. **决策过程/推理链/为什么** → ☵ 坎（kan/）
5. **时间相关/里程碑/事件日志** → ☳ 震（zhen/）
6. **人/项目/概念之间的关联** → ☴ 巽（xun/）
7. **用户偏好/情感/风格习惯** → ☱ 兑（dui/）
8. **不确定** → ☲ 离（li/），后续归类

