def notify_file(portfolio_name: str, subject: str, body: str) -> Path:
    """Write notification to a timestamped file."""
    reports_dir = get_portfolio_reports_dir(portfolio_name)
    path = reports_dir / f"notification_{timestamp_str()}.txt"
    content = f"Subject: {subject}\n{'=' * 60}\n\n{body}"
    path.write_text(content)
    logger.info(f"Notification saved: {path}")
    return path


def notify_email(
    email_config: EmailConfig,
    subject: str,
    html_body: str,
    plain_body: str,
    attachments: list[Path] | None = None,
) -> bool:
    """Send HTML email via SMTP. Returns True on success."""
    resolved = email_config.resolve_env()

    if not all([resolved.smtp_host, resolved.smtp_user, resolved.sender, resolved.recipients]):
        logger.warning("Email not configured — missing required fields.")
        return False

    msg = MIMEMultipart("mixed")
    msg["From"] = resolved.sender
    msg["To"] = ", ".join(resolved.recipients)
    msg["Subject"] = subject

    alt = MIMEMultipart("alternative")
    alt.attach(MIMEText(plain_body, "plain"))
    alt.attach(MIMEText(html_body, "html"))
    msg.attach(alt)

    if attachments:
        for filepath in attachments:
            if not filepath.exists():
                continue
            part = MIMEBase("application", "octet-stream")
            part.set_payload(filepath.read_bytes())
            encoders.encode_base64(part)
            part.add_header("Content-Disposition", f"attachment; filename={filepath.name}")
            msg.attach(part)

    try:
        with smtplib.SMTP(resolved.smtp_host, resolved.smtp_port) as server:
            if resolved.smtp_use_tls:
                server.starttls()
            if resolved.smtp_password:
                server.login(resolved.smtp_user, resolved.smtp_password)
            server.send_message(msg)
        logger.info(f"Email sent to {', '.join(resolved.recipients)}")
        return True
    except Exception as e:
        logger.error(f"Failed to send email: {e}")
        return False


def send_notification(
    portfolio_name: str,
    config: Config,
    subject: str,
    html_body: str,
    plain_body: str,
    attachments: list[Path] | None = None,
) -> None:
    """Dispatch notification based on config: file, email, or both."""
    method = config.notifications.method

    if method in ("file", "both"):
        notify_file(portfolio_name, subject, plain_body)

    if method in ("email", "both"):
        if config.email.enabled:
            notify_email(config.email, subject, html_body, plain_body, attachments)
        else:
            logger.debug("Email notifications not enabled; skipping.")


# ---------------------------------------------------------------------------
# High-level event dispatchers (called from CLI commands)
# ---------------------------------------------------------------------------

def notify_portfolio_created(
    portfolio_name: str,
    config: Config,
    method: str,
    weights: dict,
    expected_return: float,
    volatility: float,
    sharpe: float,
    risk_free_rate: float,
    investment: float,
    quantities: dict,
    html_report_path: Path | None = None,
) -> None:
    if not config.notifications.portfolio_created:
        return
    subject = f"Portfolio Created: {portfolio_name}"
    html_body, plain_body = _build_portfolio_created_email(
        portfolio_name, method, weights, expected_return, volatility,
        sharpe, risk_free_rate, investment, quantities,
    )
    attachments = [html_report_path] if html_report_path and html_report_path.exists() else None
    send_notification(portfolio_name, config, subject, html_body, plain_body, attachments)


def notify_backtest_complete(
    portfolio_name: str,
    config: Config,
    recommended_strategy: str,
    recommendation_reason: str,
    strategies: dict,
    html_report_path: Path | None = None,
) -> None:
    if not config.notifications.backtest_complete:
        return
    subject = f"Backtest Complete: {portfolio_name} — Recommended: {recommended_strategy}"
    html_body, plain_body = _build_backtest_complete_email(
        portfolio_name, recommended_strategy, recommendation_reason, strategies,
    )
    attachments = [html_report_path] if html_report_path and html_report_path.exists() else None
    send_notification(portfolio_name, config, subject, html_body, plain_body, attachments)


def notify_rebalance_reminder(
    portfolio_name: str,
    config: Config,
    needs_rebalance: bool,
    reason: str,
    max_drift: float,
    max_drift_asset: str,
    orders: list,
    estimated_cost: float,
) -> None:
    if not config.notifications.rebalance_reminder:
        return
    if needs_rebalance:
        subject = f"Rebalance Needed: {portfolio_name} (drift: {max_drift:.1%})"
    else:
        subject = f"Rebalance Check: {portfolio_name} — No Action Needed"
    html_body, plain_body = _build_rebalance_reminder_email(
        portfolio_name, needs_rebalance, reason,
        max_drift, max_drift_asset, orders, estimated_cost,
    )
    send_notification(portfolio_name, config, subject, html_body, plain_body)


def notify_performance_report(
    portfolio_name: str,
    config: Config,
    total_value: float,
    portfolio_return: float,
    benchmark_return: float | None,
    sharpe: float,
    cagr: float,
    max_drawdown: float,
    volatility: float,
    live_days: int,
    rebalance_count: int,
    html_report_path: Path | None = None,
) -> None:
    if not config.notifications.performance_report:
        return
    subject = f"Performance Report: {portfolio_name} ({portfolio_return:+.2%})"
    html_body, plain_body = _build_performance_report_email(
        portfolio_name, total_value, portfolio_return, benchmark_return,
        sharpe, cagr, max_drawdown, volatility, live_days, rebalance_count,
    )
    attachments = [html_report_path] if html_report_path and html_report_path.exists() else None
    send_notification(portfolio_name, config, subject, html_body, plain_body, attachments)
