def cmd_compare_plans(params):
    """对比目的地通讯方案"""
    destination = params.get("destination", "")
    days = params.get("days", 7)
    data_need = params.get("data_need", 500)  # MB/天
    need_calls = params.get("need_calls", False)

    if not destination:
        return {"error": "请提供目的地，如：日本、泰国、美国"}

    try:
        days = int(days)
        if days < 1: days = 1
        if days > 90: days = 90
    except (ValueError, TypeError):
        days = 7

    try:
        data_need = int(data_need)
    except (ValueError, TypeError):
        data_need = 500

    matched = match_destination(destination)
    if not matched:
        return {
            "destination": destination,
            "error": f"暂未收录{destination}的详细资费",
            "suggestion": "可参考同地区其他国家的资费标准，或咨询运营商客服",
            "general_tip": "大部分亚洲国家日套餐25元/天，欧美澳30元/天"
        }

    roaming = ROAMING_DATA.get(matched, {})
    local_sim = LOCAL_SIM_DATA.get(matched, {})
    esim_rec = ESIM_DATA.get("热门目的地推荐", {}).get(matched, "")
    
    total_data_gb = round(data_need * days / 1024, 1)

    # 1. 漫游方案
    roaming_plans = []
    for carrier, info in roaming.items():
        daily_cost = info.get("日套餐", 0)
        total = daily_cost * days
        roaming_plans.append({
            "carrier": carrier,
            "daily_cost": daily_cost,
            "total_cost": total,
            "data": info.get("日套餐流量", "未知"),
            "call_rate": info.get("通话", 0),
            "sms_rate": info.get("短信", 0),
            "note": info.get("备注", ""),
        })

    # 2. 当地SIM方案
    local_sim_plan = {}
    if local_sim:
        local_sim_plan = {
            "available": True,
            "carriers": local_sim.get("运营商", []),
            "tourist_sim": local_sim.get("游客卡", ""),
            "price_range": local_sim.get("价格区间", ""),
            "where_to_buy": local_sim.get("购买地点", ""),
            "pros_cons": local_sim.get("优缺点", ""),
            "recommendation": local_sim.get("推荐", ""),
        }

    # 3. eSIM方案
    esim_plan = {
        "recommendation": esim_rec,
        "providers": ESIM_DATA["providers"],
    }

    # 4. WiFi蛋方案
    wifi_plan = {
        "daily_rental": WIFI_EGG_DATA["日均租金"],
        "total_cost": f"{15*days}-{30*days}元",
        "devices": WIFI_EGG_DATA["可连设备"],
        "best_for": WIFI_EGG_DATA["适合场景"],
    }

    # 5. 综合推荐
    recommendations = []
    
    # 短期(≤5天) + 低流量 → 漫游日套餐
    if days <= 5 and data_need <= 500:
        recommendations.append({
            "方案": "运营商日套餐漫游",
            "理由": f"短期出行，日均{data_need}MB够用，免换卡最方便",
            "预计花费": f"{roaming_plans[0]['total_cost']}元({days}天)",
        })
    
    # 中期(5-14天) + 中等流量 → eSIM
    if 5 < days <= 14 and data_need >= 300:
        recommendations.append({
            "方案": "第三方eSIM",
            "理由": f"中等时长，eSIM免换卡且流量更多",
            "预计花费": f"$5-49(约¥35-350)",
        })
    
    # 长期(14天+) → 当地SIM
    if days > 14 and local_sim:
        recommendations.append({
            "方案": "当地SIM卡",
            "理由": f"长期停留，当地卡最划算",
            "预计花费": local_sim.get("价格区间", "因运营商而异"),
        })
    
    # 多人出行 → WiFi蛋
    recommendations.append({
        "方案": "WiFi蛋(3人+推荐)",
        "理由": "多人共享一个设备，人均成本最低",
        "预计花费": wifi_plan["total_cost"],
    })

    # 需要通话 → 漫游或运营商eSIM
    if need_calls:
        recommendations.append({
            "方案": "运营商日套餐漫游(需通话)",
            "理由": "需要接打国内电话/收短信验证码，只有漫游能满足",
            "预计花费": f"{roaming_plans[0]['total_cost']}元+通话费",
        })

    return {
        "destination": destination,
        "matched_country": matched,
        "days": days,
        "daily_data_need": f"{data_need}MB",
        "total_data_need": f"{total_data_gb}GB",
        "roaming_plans": roaming_plans,
        "local_sim": local_sim_plan,
        "esim": esim_plan,
        "wifi_egg": wifi_plan,
        "recommendations": recommendations,
    }


