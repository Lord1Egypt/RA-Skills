#!/bin/bash
# ==============================================
# 多智能体小说创作系统 - 完整流水线执行
# ==============================================

set -e

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"

if [ $# -lt 1 ]; then
    echo "用法: $0 <项目目录> [章节范围]"
    echo "示例: $0 ../my-novel 1-3"
    exit 1
fi

PROJECT_DIR="$1"
CHAPTER_RANGE="${2:-1-10}"

echo "=============================================="
echo "🚀 多智能体小说创作系统 - 完整流水线"
echo "=============================================="
echo ""
echo "📁 项目目录: $PROJECT_DIR"
echo "📖 章节范围: $CHAPTER_RANGE"
echo ""
echo "⚠️  注意："
echo "   这个脚本会按顺序执行所有步骤"
echo "   如果某一步需要人工编辑，脚本会等待你确认"
echo ""
read -p "按回车键开始，或 Ctrl+C 取消..."
echo ""

# 记录开始时间
START_TIME=$(date +%s)

# ==============================================
# 第1步：世界观构建
# ==============================================
echo "=============================================="
echo "🌍 第1步：世界观构建"
echo "=============================================="
"$SCRIPT_DIR/step1-world.sh" "$PROJECT_DIR"
echo ""
read -p "完成世界观设定后，按回车键继续..."
echo ""

# ==============================================
# 第2步：角色设计
# ==============================================
echo "=============================================="
echo "👥 第2步：角色设计"
echo "=============================================="
"$SCRIPT_DIR/step2-character.sh" "$PROJECT_DIR"
echo ""
read -p "完成角色设计后，按回车键继续..."
echo ""

# ==============================================
# 第3步：情节规划
# ==============================================
echo "=============================================="
echo "📑 第3步：情节规划"
echo "=============================================="
"$SCRIPT_DIR/step3-plot.sh" "$PROJECT_DIR" "$CHAPTER_RANGE"
echo ""
read -p "完成大纲设计后，按回车键继续..."
echo ""

# ==============================================
# 第4步：正文创作
# ==============================================
echo "=============================================="
echo "✍️  第4步：正文创作"
echo "=============================================="
"$SCRIPT_DIR/step4-write.sh" "$PROJECT_DIR" "$CHAPTER_RANGE"
echo ""
read -p "完成正文创作后，按回车键继续..."
echo ""

# ==============================================
# 第5步：质量评审
# ==============================================
echo "=============================================="
echo "🔍 第5步：质量评审"
echo "=============================================="
"$SCRIPT_DIR/step5-review.sh" "$PROJECT_DIR" "$CHAPTER_RANGE"
echo ""

# 计算耗时
END_TIME=$(date +%s)
DURATION=$((END_TIME - START_TIME))
MINUTES=$((DURATION / 60))
SECONDS=$((DURATION % 60))

echo "=============================================="
echo "🎉 完整流水线执行完成！"
echo "=============================================="
echo ""
echo "⏱️  总耗时: ${MINUTES}分${SECONDS}秒"
echo "📁 项目目录: $PROJECT_DIR"
echo ""
echo "📊 产出文件："
echo "   ✅ $PROJECT_DIR/01-世界观设定.md"
echo "   ✅ $PROJECT_DIR/02-角色档案.md"
echo "   ✅ $PROJECT_DIR/03-分章大纲/"
echo "   ✅ $PROJECT_DIR/04-正文初稿/"
echo "   ✅ $PROJECT_DIR/05-评审报告/"
echo ""
echo "📝 下一步："
echo "   根据评审意见优化正文，保存到 06-最终定稿/"
echo "   然后可以开始下一批章节的创作！"
echo ""
echo "祝您创作愉快！ 🎉"
echo ""
