def parse_layers(layers_str: str) -> List[int]:
    try:
        return [int(x.strip()) for x in layers_str.split(",")]
    except ValueError:
        raise ValueError(f"Invalid layers format: {layers_str}. Use: 768,32,16,8,4,3")


def generate_layers(input_size: int, hidden_size: int, output_size: int) -> List[int]:
    return [input_size, hidden_size, hidden_size // 2, hidden_size // 4, output_size]


def create_kan(
    name: str,
    role: str,
    agent: str = "system",
    input_size: int = 768,
    output_size: int = 3,
    hidden_size: int = 32,
    grid_size: int = 5,
    k: int = 3,
    layers: Optional[List[int]] = None,
    output_dir: Optional[str] = None
) -> Path:
    if output_dir is None:
        output_dir = Path(".")
    else:
        output_dir = Path(output_dir)

    kan_dir = output_dir / name
    models_dir = kan_dir / "models"
    data_dir = kan_dir / "data"

    for d in [kan_dir, models_dir, data_dir]:
        d.mkdir(parents=True, exist_ok=True)

    if layers is None:
        layers = generate_layers(input_size, hidden_size, output_size)

    if layers[-1] != output_size:
        layers[-1] = output_size

    config = DEFAULT_CONFIG.copy()
    config["name"] = name
    config["role"] = role
    config["agent"] = agent
    config["input_size"] = input_size
    config["output_size"] = output_size
    config["hidden_size"] = hidden_size
    config["grid_size"] = grid_size
    config["k"] = k
    config["layers"] = layers

    config_path = kan_dir / "config.json"
    with open(config_path, "w") as f:
        json.dump(config, f, indent=2)

    arch_str = " -> ".join(str(x) for x in layers)
    model_code = KAN_MODEL_TEMPLATE.replace("{name}", name).replace("{role}", role).replace("{arch_str}", arch_str)

    model_path = models_dir / f"{name}_kan.py"
    with open(model_path, "w") as f:
        f.write(model_code)

    return kan_dir


