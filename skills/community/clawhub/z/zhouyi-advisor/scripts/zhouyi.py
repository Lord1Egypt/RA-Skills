def main():
    import sys
    if sys.platform == "win32":
        try:
            sys.stdout.reconfigure(encoding="utf-8", errors="replace")
        except Exception:
            pass

    parser = argparse.ArgumentParser(description="周易智慧顾问 (Zhou Yi Wisdom Advisor)")
    parser.add_argument("--mode", required=True,
                        choices=["divine", "query", "demo", "philosophy"],
                        help="功能模式")
    parser.add_argument("--method", default="coin",
                        choices=["coin", "number", "time"],
                        help="起卦方式")
    parser.add_argument("--numbers", default="", help="数字起卦的三个数字，逗号分隔")
    parser.add_argument("--question", default="", help="占问问题")
    parser.add_argument("--gua", type=int, default=0, help="查询的卦序(1-64)")
    parser.add_argument("--output", default="zhouyi_report.html", help="输出路径")
    args = parser.parse_args()

    output_path = args.output
    if not output_path.startswith("/") and not output_path.startswith("C:"):
        output_path = str(Path(os.getcwd()) / output_path)

    if args.mode == "demo":
        html = generate_demo_html()
        print(f"☯️ 周易智慧顾问 · 演示模式")
        print(f"📜 随机演示卦象解读与起卦过程")

    elif args.mode == "divine":
        method_name_map = {"coin": "铜钱起卦", "number": "数字起卦", "time": "时间起卦"}
        method_name = method_name_map[args.method]

        if args.method == "coin":
            yaos, ben_yao, bian_yao = divine_by_coin()
        elif args.method == "number":
            if not args.numbers:
                print("❌ 数字起卦需要 --numbers 参数（如 --numbers 25,36,17）")
                return
            try:
                nums = [int(x.strip()) for x in args.numbers.split(",")]
                if len(nums) != 3:
                    print("❌ 需要3个数字，逗号分隔")
                    return
                yaos, ben_yao, bian_yao = divine_by_numbers(*nums)
            except ValueError:
                print("❌ 数字格式错误")
                return
        else:  # time
            yaos, ben_yao, bian_yao = divine_by_time()

        ben_gua = find_gua(ben_yao)
        bian_gua = find_gua(bian_yao) if bian_yao != ben_yao else None

        if ben_gua is None:
            print(f"⚠️ 找不到本卦（yao={ben_yao}），使用演示数据")
            ben_gua = HEXAGRAMS[1].copy()
            ben_gua["id"] = 1

        changing_positions = [i+1 for i,y in enumerate(yaos) if y["changing"]]
        print(f"\n☯️ {method_name}")
        print(f"📜 本卦：第{ben_gua['id']}卦 {ben_gua['name']} {ben_gua.get('symbol','')}")
        print(f"   卦辞：{ben_gua.get('gua_ci_bai','')[:80]}...")
        if bian_gua:
            print(f"🔄 变卦：第{bian_gua['id']}卦 {bian_gua['name']} {bian_gua.get('symbol','')}")
        if changing_positions:
            print(f"⚡ 动爻：第{'、'.join(str(p) for p in changing_positions)}爻")
        if args.question:
            print(f"💭 占问：{args.question}")

        html = generate_divination_html(method_name, yaos, ben_gua, bian_gua, args.question)

    elif args.mode == "query":
        if args.gua < 1 or args.gua > 64:
            print("❌ 卦序必须在1-64之间")
            return
        gua = HEXAGRAMS[args.gua]
        print(f"\n📖 第{args.gua}卦 {gua['name']} {gua.get('symbol','')}")
        print(f"   卦辞：{gua.get('gua_ci','')}")
        print(f"   白话：{gua.get('gua_ci_bai','')[:100]}...")
        print(f"   大象：{gua.get('xiang','')}")
        html = generate_query_html(args.gua)

    else:
        html = generate_demo_html()

    with open(output_path, "w", encoding="utf-8") as f:
        f.write(html)
    print(f"\n✅ 报告已生成：{output_path}")


if __name__ == "__main__":
    main()
