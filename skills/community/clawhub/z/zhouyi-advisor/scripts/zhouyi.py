def generate_query_html(gua_id):
    """生成单卦查询报告"""
    if gua_id not in HEXAGRAMS:
        return f"<html><body><h1>未找到第{gua_id}卦</h1></body></html>"

    gua = HEXAGRAMS[gua_id].copy()
    gua["id"] = gua_id

    # 模拟一个简单的无变卦展示
    yaos = []
    yao_str = gua["yao"]
    for i, c in enumerate(yao_str):
        val = int(c)
        yaos.append({
            "value": val, "changing": False,
            "type": "少阳" if val == 1 else "少阴",
            "symbol": "⚊" if val == 1 else "⚋",
            "desc": "查询模式"
        })

    return generate_divination_html("单卦查询", yaos, gua, None, f"查询第{gua_id}卦 {gua['name']}")


