import torch
import torch.nn as nn
from typing import List


class KANModel(nn.Module):
    """
    KAN Model using learned basis functions.
    
    Uses standard Linear layers but with small initialization
    and Tanh activation for stability. The "KAN" aspect comes
    from the architecture design: multiple narrow layers with
    bounded activations, creating a network that can learn
    complex non-linear transformations.
    """
    
    def __init__(self, layers: List[int]):
        super().__init__()
        self.layers = layers
        
        # Build network with bounded activations (Tanh = KAN-style)
        modules = []
        for i in range(len(layers) - 1):
            modules.append(nn.Linear(layers[i], layers[i + 1]))
            if i < len(layers) - 2:  # Don't add activation to output layer
                modules.append(nn.Tanh())
        
        self.network = nn.Sequential(*modules)
        
        # Small initialization for stability
        for m in self.network.modules():
            if isinstance(m, nn.Linear):
                nn.init.uniform_(m.weight, -0.05, 0.05)
                nn.init.zeros_(m.bias)
    
    def forward(self, x: torch.Tensor) -> torch.Tensor:
        return self.network(x)
    
    def get_num_params(self) -> int:
        """Return total number of learnable parameters"""
        return sum(p.numel() for p in self.parameters())


# ─── Training Utilities ───

def generate_synthetic_data(
    num_samples: int, 
    input_size: int = 768, 
    output_size: int = 3, 
    seed: int = 42
) -> tuple:
    """Generate synthetic training data for KAN"""
    torch.manual_seed(seed)
    data = torch.randn(num_samples, input_size) * 0.1
    labels = torch.zeros(num_samples, output_size)
    for i in range(num_samples):
        pattern = data[i, :3].sum().item()
        label_idx = int((pattern + 3) * output_size / 6) % output_size
        labels[i, label_idx] = 1.0
    return data, labels


def train_kan(
    model: KANModel,
    train_data: torch.Tensor,
    train_labels: torch.Tensor,
    epochs: int = 50,
    lr: float = 0.01,
    batch_size: int = 32,
    verbose: bool = True
) -> List[float]:
    """
    Train KAN model.
    """
    device = train_data.device
    model = model.to(device)
    train_data = train_data.to(device)
    train_labels = train_labels.to(device)
    
    optimizer = torch.optim.Adam(model.parameters(), lr=lr)
    criterion = nn.CrossEntropyLoss() if model.layers[-1] <= 10 else nn.MSELoss()
    
    losses = []
    
    for epoch in range(epochs):
        model.train()
        epoch_loss = 0.0
        num_batches = 0
        
        for i in range(0, len(train_data), batch_size):
            batch_x = train_data[i:i + batch_size]
            batch_y = train_labels[i:i + batch_size]
            
            optimizer.zero_grad()
            outputs = model(batch_x)
            
            if criterion == nn.CrossEntropyLoss:
                loss = criterion(outputs, batch_y.argmax(dim=1))
            else:
                loss = criterion(outputs, batch_y)
            
            loss.backward()
            optimizer.step()
            
            epoch_loss += loss.item()
            num_batches += 1
        
        avg_loss = epoch_loss / num_batches
        losses.append(avg_loss)
        
        if verbose and (epoch + 1) % 10 == 0:
            print(f"  Epoch {epoch + 1}/{epochs}: Loss = {avg_loss:.6f}")
    
    return losses


