def normalize_type(type_key: str) -> str:
    value = (type_key or "doc").strip()
    if value in TEMPLATES:
        return value
    return ALIASES.get(value.lower(), ALIASES.get(value, "doc"))


def render(type_key: str, **values: str) -> dict:
    key = normalize_type(type_key)
    spec = TEMPLATES[key]
    defaults = {
        "title": values.get("title") or "未命名资料",
        "project_id": values.get("project_id") or "未指定",
        "source_id": values.get("source_id") or "未提及",
        "source_path": values.get("source_path") or "未提及",
        "source_url": values.get("source_url") or "未提及",
        "source_commit": values.get("source_commit") or "未提及",
    }
    return {
        "success": True,
        "module_version": COMMON_MODULE_VERSION,
        "type_key": key,
        "type_name": spec["name"],
        "target_folder": spec["folder"],
        "must_capture_fields": spec["fields"],
        "rules": COMMON_RULES,
        "quality_checklist": QUALITY_CHECKLIST,
        "markdown_template": spec["template"].format(**defaults),
    }


