def has_garbled_title(title):
    """
    检测标题是否含乱码（Cyrillic 等 Mojibake 字符）
    人民日报海外版等 GBK 编码页面被误读为 UTF-8 时会生成此类字符
    """
    if not title:
        return False
    # Cyrillic 范围：а-яА-Я
    if re.search(r'[а-яА-Я]', title):
        return True
    # 非CJK/非ASCII 比例超过 50% 也判定为乱码
    non_cjk_ascii = sum(1 for c in title
                        if not (0x4e00 <= ord(c) <= 0x9fff or 0x3000 <= ord(c) <= 0x303f
                                or ord(c) == 0x20 or ord(c) == 0x3001
                                or 0x0020 <= ord(c) <= 0x007e))
    if len(title) > 5 and non_cjk_ascii / len(title) > 0.5:
        return True
    return False


