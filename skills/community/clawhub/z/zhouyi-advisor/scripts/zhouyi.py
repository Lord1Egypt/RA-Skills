def divine_by_coin():
    """铜钱起卦：模拟三枚铜钱掷六次"""
    yaos = []
    for i in range(6):
        # 三枚铜钱：3=正面(阳), 2=反面(阴)
        coins = [random.choice([3, 2]) for _ in range(3)]
        total = sum(coins)
        if total == 18:  # 三正 → 老阳 (变爻)
            yaos.append({"value": 1, "changing": True, "type": "老阳", "symbol": "⚊○⚊", "desc": "三正→老阳（变阴）"})
        elif total == 19:  # 两正一反 → 少阳
            yaos.append({"value": 1, "changing": False, "type": "少阳", "symbol": "⚊", "desc": "两正一反→少阳"})
        elif total == 20:  # 一正两反 → 少阴
            yaos.append({"value": 0, "changing": False, "type": "少阴", "symbol": "⚋", "desc": "一正两反→少阴"})
        else:  # 17=三反 → 老阴 (变爻)
            yaos.append({"value": 0, "changing": True, "type": "老阴", "symbol": "⚋×⚋", "desc": "三反→老阴（变阳）"})

    ben_yao = "".join(str(y["value"]) for y in yaos)
    bian_yao = "".join(str(1 - y["value"]) if y["changing"] else str(y["value"]) for y in yaos)

    return yaos, ben_yao, bian_yao


def divine_by_numbers(a, b, c):
    """数字起卦（梅花易数法）"""
    shang_num = a % 8  # 上卦
    xia_num = b % 8    # 下卦
    dong_yao = c % 6   # 动爻

    shang_gua = 8 if shang_num == 0 else shang_num
    xia_gua = 8 if xia_num == 0 else xia_num
    dong_pos = 6 if dong_yao == 0 else dong_yao

    # 构建本卦的六爻（下卦在下，上卦在上）
    # 数字对应的八卦二进制：1乾111, 2兑011, 3离101, 4震001, 5巽110, 6坎010, 7艮100, 8坤000
    num_to_yao_str = {
        1: "111", 2: "011", 3: "101", 4: "001",
        5: "110", 6: "010", 7: "100", 8: "000"
    }
    xia_str = num_to_yao_str[xia_gua]
    shang_str = num_to_yao_str[shang_gua]
    ben_yao = xia_str + shang_str  # 下卦在前（初爻开始）

    # 构建变卦：变动爻位
    bian_list = list(ben_yao)
    idx = dong_pos - 1
    bian_list[idx] = "1" if bian_list[idx] == "0" else "0"
    bian_yao = "".join(bian_list)

    # 构建爻数据
    yaos = []
    for i in range(6):
        changing = (i == idx)
        val = int(ben_yao[i])
        yaos.append({
            "value": val, "changing": changing,
            "type": ("老阳" if val == 1 else "老阴") if changing else ("少阳" if val == 1 else "少阴"),
            "symbol": (f"{'⚊○⚊' if val == 1 else '⚋×⚋'}") if changing else ("⚊" if val == 1 else "⚋"),
            "desc": f"数字起卦{'（动爻）' if changing else ''}"
        })

    return yaos, ben_yao, bian_yao


def divine_by_time():
    """时间起卦"""
    now = datetime.datetime.now()
    year = now.year
    month = now.month
    day = now.day
    hour = now.hour

    shang_num = (year + month + day) % 8
    xia_num = (year + month + day + hour) % 8
    dong_yao = (year + month + day + hour) % 6

    return divine_by_numbers(
        year + month + day,
        year + month + day + hour,
        dong_yao + 1  # 避免0
    )


def find_gua(yao_str):
    """根据六爻字符串查找卦"""
    for gid, gdata in HEXAGRAMS.items():
        if gdata["yao"] == yao_str:
            gdata_copy = gdata.copy()
            gdata_copy["id"] = gid
            return gdata_copy
    return None


