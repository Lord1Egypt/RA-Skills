#!/bin/bash
# 八卦记忆系统初始化脚本
# 用法: bash scripts/init_bagua.sh [workspace目录]
# 默认在当前目录下创建 memory/ 结构

WORKSPACE="${1:-.}"
MEMORY_DIR="$WORKSPACE/memory"
BAGUA_DIR="$MEMORY_DIR/bagua"

mkdir -p "$BAGUA_DIR"/{qian,kun,li,kan,zhen,xun,gen,dui}

# 创建 README.md（详细架构参考）
cat > "$BAGUA_DIR/README.md" << 'BAGUAEOF'
# 🔮 八卦记忆系统 (Bagua Memory)

> 本文件是八卦记忆系统的详细参考文档，需要深入理解分类规则和生命周期时再读取。

## 两层结构

> 已简化：新信息直接写入对应卦位，无中间层。

## 八卦详解

### ☰ 乾 (qian/) — 天/规则
系统指令、元认知、行为规则。Agent 自身的运行约束和配置。
示例：AGENTS.md 中的规则、Skill 配置、系统限制。

### ☷ 坤 (kun/) — 地/承载
知识库、事实、用户信息。稳定的、不容易变的信息。
示例：项目文档摘要、API 说明、用户基本信息、Agent 列表。

### ☲ 离 (li/) — 火/明
近期热记忆，7 天内活跃的重要信息。这是"短期长期记忆"。
示例：最近的重要发现、正在进行的项目状态、新学到的知识。

### ☵ 坎 (kan/) — 水/流
对话流摘要、决策链。记录"为什么"而不是"是什么"。
示例：架构决策过程、问题排查思路、方案选型的推理链。

### ☳ 震 (zhen/) — 动/触发
事件时间线、日志。按时间顺序记录发生的事情。
示例：部署记录、里程碑、Bug 修复历史、版本变更。

### ☴ 巽 (xun/) — 风/散
关联网络、人脉、项目关系。描述事物之间的联系。
示例：A 项目依赖 B 服务、张三负责 C 模块、这两个方案有关联。

### ☶ 艮 (gen/) — 山/止
归档记忆。超过 7 天无引用的冷却记忆暂时存放在此。
再次被引用时会升级回原卦位。超过 30 天的内容压缩为摘要。

### ☱ 兑 (dui/) — 泽/悦
用户偏好、情感记忆、风格习惯。关于"人"的个性化信息。
示例：用户喜欢深色主题、习惯晚上工作、偏好简洁的回复风格。

## 卦变关联（检索增强）

检索时按意图定位主卦，通过卦变预取关联记忆：

| 场景 | 主卦 + 辅卦 | 卦名 | 说明 |
|------|------------|------|------|
| 管理 Agent | 乾 + 巽 | 天风姤 | 规则 + 关联 |
| 查日程/事件 | 震 + 坎 | 水雷屯 | 事件 + 对话流 |
| 用户偏好 | 兑 + 坤 | 泽地萃 | 偏好 + 知识库 |
| 故障排查 | 乾 + 震 | 天雷无妄 | 规则 + 事件 |
| 项目复盘 | 巽 + 离 | 风火家人 | 关联 + 热记忆 |
| 知识学习 | 坤 + 乾 | 地天泰 | 知识 + 规则 |
| 情感关怀 | 兑 + 离 | 泽火革 | 偏好 + 热记忆 |
| 长期规划 | 艮 + 坎 | 山水蒙 | 归档 + 决策 |

## 生命周期

```
新信息 → 直接写入对应卦位
→ 7天无引用 → gen/（归档）→ 30天无引用 → 压缩摘要
→ 再次引用 → 升回原卦位
```

## 写入格式

```markdown
## YYYY-MM-DD HH:MM

**来源**: 对话 / 日程 / 文件 / 主动观察

记忆内容...

**标签**: #标签1 #标签2
```
